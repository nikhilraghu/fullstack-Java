package S01L10_Naming_Convention;

public class S01L10_Naming_Convention {
    public static void main(String[] args) {

//     Classes and Interfaces:

//        Class Names:
//        Use PascalCase (capitalize the first letter of each word).
//        Examples: CustomerAccount, StudentManager
//
//        Interface Names:
//        Also use PascalCase.
//        Examples: Readable, Persistable


//     Methods:
//
//        Method Names: Use camelCase (capitalize the first letter of each word except the first).
//        Examples: calculateTotal(), findUserById()


//     Variables:
//
//        Variable Names:
//        Use camelCase.
//        Examples: totalAmount, userName

//     Constants:

//        Constant Names:
//        Use UPPER_SNAKE_CASE (all uppercase letters with words separated by underscores).
//        Examples: MAX_VALUE, DEFAULT_TIMEOUT


//       Packages:
//
//        Package Names:
//        Use all lowercase letters and separate words with dots.
//        Examples: com.example.myapp, org.apache.commons
//

//        Enums:

//        Enum Names:
//        Use PascalCase.
//        Examples: DayOfWeek, PaymentStatus

//        Enum Constants:
//        Use UPPER_SNAKE_CASE (all uppercase letters with underscores separating words).
//        Examples: MONDAY, PENDING

//        Generic Type Parameters:

//        Type Parameters:
//        Typically use single uppercase letters. Common conventions include:
//        T for a type
//        E for an element
//        K for a key
//        V for a value
//        Examples: public class Box<T>, public interface Map<K, V>

    }
}
