package S02L08_else_if;

public class S02L08_ELSE_IF {
    public static void main(String[] args) {
         int x = 4;

         if(x == 5) {
             System.out.println("x is 5");
         } else if (x > 5) {
             System.out.println("x is greatere than 5");
         } else {
             System.out.println("x is smaller than 5");
         }

    }
}
