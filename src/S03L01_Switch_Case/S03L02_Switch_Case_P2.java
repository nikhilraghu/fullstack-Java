package S03L01_Switch_Case;

public class S03L02_Switch_Case_P2 {
    public static void main(String[] args) {

//      Ques - condition
        //if xis 1
        //if x is 3
        //if x is othere then 1, 2, 4


        int x = 4;
        switch (x) {
            case 1 -> System.out.println("X is 1");
            case 2 -> System.out.println("X is 2");
            case 3 -> System.out.println("X is 3");
            default -> System.out.println("X is  other then 1, 2, 3");
        }



    }
}
