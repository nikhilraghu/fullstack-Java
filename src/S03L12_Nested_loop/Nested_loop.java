package S03L12_Nested_loop;

public class Nested_loop {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j <10 ; j++) {
                System.out.println("value of i: "+ i + " and "+ "value of j"+ j);
            }
            System.out.println("***********");
        }
    }
}
