package S03LO5_for_loop;

public class S03LO5_for_loop_p1 {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++){
            System.out.println(i);
        }

//--------------------------------------------------
        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }

//--------------------------------------------------
        for (int i = 0; i <= 20; i= i+2) {
            System.out.println(i);
        }
    }

}
